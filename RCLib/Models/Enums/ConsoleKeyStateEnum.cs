namespace RCLib.Models
{
    /// <summary>
    /// Enum for different key states
    /// </summary>
    public enum ConsoleKeyStateEnum
    {
        /// <summary>
        /// The key is not pressed
        /// </summary>
        NONE = 0,

        /// <summary>
        /// The key is pressed
        /// </summary>
        PRESSED = 1,

        /// <summary>
        /// The key is released
        /// </summary>
        RELEASED = 2
    }
}
